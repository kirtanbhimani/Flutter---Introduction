// Division of Two Numbers.

// ignore: unused_import
import 'dart:io';

void main(List<String> args) {
  int a = 10, b = 5;
  print("Addition of $a and $b = ${a + b}");
  print("Substraction of $a and $b = ${a - b}");
  print("Multiplication of $a and $b = ${a * b}");
  print("Division of $a and $b = ${a / b}");
}
